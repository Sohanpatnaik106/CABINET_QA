            relevance_score = self.model.sigmoid(relevance_logit)
            gaussian_relevance_score = self.stricter_gaussian_dist.log_prob(relevance_logit).exp().to(token_scores_1.device)
            
            # token_scores = self.model.sigmoid(token_logits / torch.mean(torch.abs(token_logits), dim = -1).unsqueeze(-1)).unsqueeze(-1)

